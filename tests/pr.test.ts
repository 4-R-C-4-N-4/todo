// End-to-end: CLI refuses to run off a todo/<id> branch.
// ---------------------------------------------------------------------------

function todoCli(
	dir: string,
	args: string[],
): Promise<{ status: number; stdout: string; stderr: string }> {
	const cliPath = join(__dirname, "..", "dist", "cli.js");
	return new Promise((resolve) => {
		const child = spawn("node", [cliPath, ...args], {
			cwd: dir,
			env: { ...process.env },
		});
		let stdout = "";
		let stderr = "";
		child.stdout.on("data", (d) => {
			stdout += d.toString();
		});
		child.stderr.on("data", (d) => {
			stderr += d.toString();
		});
		child.on("close", (code) => {
			resolve({ status: code ?? 1, stdout, stderr });
		});
	});
}

describe("CLI pr (end-to-end)", () => {
	let dir: string;

	beforeEach(() => {
		dir = makeTempDir();
		initGitRepo(dir);
		makeTodoDir(dir);
		writeConfig(dir);
		makeCommit(dir, "seed.txt", "seed");
	});

	afterEach(() => {
		removeTempDir(dir);
	});

	it("refuses to run from main", async () => {
		const res = await todoCli(dir, ["pr"]);
		expect(res.status).toBe(1);
		expect(res.stderr).toContain("todo/<id>");
	});

	it("refuses when branch references a missing ticket", async () => {
		const { execFileSync } = await import("node:child_process");
		execFileSync("git", ["checkout", "-b", "todo/nonexistent"], { cwd: dir });
		const res = await todoCli(dir, ["pr"]);
		expect(res.status).toBe(1);
		expect(res.stderr).toContain("nonexistent");
	});
});
